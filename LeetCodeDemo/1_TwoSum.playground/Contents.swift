import UIKit

var str = "two sum"

class TwoSum {
    func twoSum(_ nums: [Int], _ target: Int) -> [Int] {
        var dic = [Int:Int]()
        for (i, num) in nums.enumerated() {
            if let index = dic[target - num] {
                return [i, index]
            }
            dic[num] = i
        }
        return [-1, -1]
    }
}

let twoSum = TwoSum()
let arr = twoSum.twoSum([1, 2, 4, 3, 5], 9)

